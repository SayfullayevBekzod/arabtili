from django.conf import settings
from django.conf.urls.static import static
from django.contrib import admin
from django.urls import path, include
from django.contrib.auth import views as auth_views
from django.views.generic import TemplateView

urlpatterns = [
    path("admin/", admin.site.urls),
    path("", include("arab.urls")),
    path("sw.js", (TemplateView.as_view(template_name="sw.js", content_type='application/javascript', ))),
]

# Add allauth URLs but exclude login/register to avoid conflicts
urlpatterns += [
    path("accounts/", include("allauth.urls")),
    # Standard Auth URLs (Global namespace for reverse matches)
    path("accounts/password-reset/", auth_views.PasswordResetView.as_view(), name="password_reset"),
    path("accounts/password-reset/done/", auth_views.PasswordResetDoneView.as_view(), name="password_reset_done"),
    path("accounts/reset/<uidb64>/<token>/", auth_views.PasswordResetConfirmView.as_view(), name="password_reset_confirm"),
    path("accounts/reset/done/", auth_views.PasswordResetCompleteView.as_view(), name="password_reset_complete"),
]

if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)

# Custom error handlers
handler404 = 'arab.views.custom_404'
handler500 = 'arab.views.custom_500'
